export type CloudflareProductKey =
  (typeof CLOUDFLARE_PRODUCT_DEFINITIONS)[number]["key"];

type CloudflareProductDefinition =
  (typeof CLOUDFLARE_PRODUCT_DEFINITIONS)[number];

export const CLOUDFLARE_PRODUCT_LABELS = Object.fromEntries(
  CLOUDFLARE_PRODUCT_DEFINITIONS.map(({ key, label }) => [key, label]),
) as Record<CloudflareProductKey, string>;

export const CLOUDFLARE_PRODUCT_BY_KEY = Object.fromEntries(
  CLOUDFLARE_PRODUCT_DEFINITIONS.map((definition) => [
    definition.key,
    definition,
  ]),
) as Record<CloudflareProductKey, CloudflareProductDefinition>;

export function isCloudflareProductKey(
  value: string,
): value is CloudflareProductKey {
  return value in CLOUDFLARE_PRODUCT_BY_KEY;
}

export function listCloudflareProductDefinitions(): CloudflareProductDefinition[] {
  return [...CLOUDFLARE_PRODUCT_DEFINITIONS];
}
